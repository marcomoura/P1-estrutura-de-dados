
public class Produto {

    private int kilogramas;

    private int preco;

    private String nome;

    public Produto () {
    }

    public int getKilogramas () {
        return kilogramas;
    }

    public void setKilogramas (int val) {
        this.kilogramas = val;
    }

    public String getNome () {
        return nome;
    }

    public void setNome (String val) {
        this.nome = val;
    }

    public int getPreco () {
        return preco;
    }

    public void setPreco (int val) {
        this.preco = val;
    }

}

