
public class CargoColaborador {

  private Colaborador colaborador;	
  private Cargo cargo;	
  private String inicio;
  private String fim;
	
  public Colaborador getColaborador () {
      return colaborador;
  }

  public void setColaborador (Colaborador val) {
      this.colaborador = val;
  }

  public Colaborador getCargo () {
      return colaborador;
  }

  public void setCargo (Cargo cargo) {
      this.cargo = cargo;
  }

  public String getFim () {
      return fim;
  }

  public void setFim (String val) {
      this.fim = val;
  }

  public String getInicio () {
      return inicio;
  }

  public void setInicio (String val) {
      this.inicio = val;
  }

}
